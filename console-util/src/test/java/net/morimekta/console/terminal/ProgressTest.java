    @Test
    public void testProgress() {
        FakeClock clock = new FakeClock();

        try (Progress p = new Progress(new Terminal(console.tty()),
                                       null,
                                       () -> 128,
                                       clock,
                                       Progress.Spinner.ASCII,
                                       "Title",
                                       127)) {
            clock.tick(200);
            p.accept(50);
            clock.tick(300);
            p.accept(126);
            clock.tick(400);
            p.accept(127);
        }

        assertThat(stripNonPrintable(console.output()),
                   is("\r" +
                      "Title: [----------------------------------------------------------------------------------------------------]   0% |\r" +
                      "Title: [#######################################-------------------------------------------------------------]  39% /\r" +
                      "Title: [###################################################################################################-]  99% -\r" +
                      "Title: [####################################################################################################] 100% v @  0.9  s"));
    }

